package com.cn.elp.POJO;


public class Flawtype {

  private int flawTypeId;
  private String flawName;
  private String status;


  public int getFlawTypeId() {
    return flawTypeId;
  }

  public void setFlawTypeId(int flawTypeId) {
    this.flawTypeId = flawTypeId;
  }


  public String getFlawName() {
    return flawName;
  }

  public void setFlawName(String flawName) {
    this.flawName = flawName;
  }


  public String getStatus() {
    return status;
  }

  public void setStatus(String status) {
    this.status = status;
  }

}
